weekdagen = ['maandag', 'dinsdag', 'woensdag', 'donderdag', 'vrijdag', 'zaterdag', 'zondag']

print(f"Dat zijn alle dagen in het week{weekdagen}")
print(f"Dit zijn alle werkdagen {weekdagen[0:5]}")
print(f"De weekenddagen zijn {weekdagen[5:7]}")
print(f"De alle dagen van de week in omgekeerde volgorde{weekdagen[::-1]}")
print(f"Dit zijn alle werk dagen in omgekeerd volgorde{weekdagen[4::-1]}")
print(f"Dit zijn weekenddage in omgekeerd volgorde{weekdagen[6:4:-1]}")